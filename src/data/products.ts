// Helper Functions
// ============================================================

export const getProductById = (id: string): Product | undefined => {
  return products.find(p => p.id === id);
};

export const getProductsByCategory = (category: ProductCategory): Product[] => {
  return products.filter(p => p.category === category);
};

export const getProductsBySubcategory = (subcategory: string): Product[] => {
  return products.filter(p => p.subcategory.toLowerCase() === subcategory.toLowerCase());
};

export const getProductsByTag = (tag: string): Product[] => {
  return products.filter(p => p.tags?.includes(tag as any));
};

export const searchProducts = (query: string): Product[] => {
  const lowercaseQuery = query.toLowerCase();
  return products.filter(p => 
    p.name.toLowerCase().includes(lowercaseQuery) ||
    p.description.toLowerCase().includes(lowercaseQuery) ||
    p.category.toLowerCase().includes(lowercaseQuery) ||
    p.subcategory.toLowerCase().includes(lowercaseQuery) ||
    p.brand.toLowerCase().includes(lowercaseQuery)
  );
};

export const getRandomProducts = (count: number, excludeIds: string[] = []): Product[] => {
  const available = products.filter(p => !excludeIds.includes(p.id));
  const shuffled = [...available].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const getProductsByDietary = (dietary: string): Product[] => {
  return products.filter(p => p.attributes?.dietary?.includes(dietary.toLowerCase()));
};

export const getProductsByAllergenFree = (allergen: string): Product[] => {
  return products.filter(p => 
    p.category === 'food' && 
    !p.attributes?.allergens?.some(a => a.toLowerCase().includes(allergen.toLowerCase()))
  );
};

export const getProductsBySkinType = (skinType: string): Product[] => {
  return products.filter(p => p.attributes?.skinType?.includes(skinType.toLowerCase()));
};

export const getProductsByPetType = (petType: string): Product[] => {
  return products.filter(p => p.attributes?.petType === petType.toLowerCase());
};

export const getProductsByAgeRange = (age: number): Product[] => {
  return products.filter(p => {
    const range = p.attributes?.ageRange;
    if (!range) return false;
    const [min, max] = range.split('-').map(Number);
    return age >= min && age <= max;
  });
};

export const getProductsByActivity = (activity: string): Product[] => {
  return products.filter(p => 
    p.attributes?.activityType?.toLowerCase().includes(activity.toLowerCase())
  );
};

// Category counts for display
export const getCategoryCounts = (): Record<ProductCategory, number> => {
  return products.reduce((acc, p) => {
    acc[p.category] = (acc[p.category] || 0) + 1;
    return acc;
  }, {} as Record<ProductCategory, number>);
};
